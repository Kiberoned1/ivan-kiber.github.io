//саздаём масив
var animals = ['cat','horse','dog']
alert(animals)

//push() - доб. элемент в конц масива
animals.push('fish')
alert(animals)

// unshift()-доб. элемент в начало масива
animals.unshift('goose')
alert(animals)

//pop удаляет эл. из конца масива
animals.pop()
alert(animals)

// shift() - удаляет ел. из начала массива
animals.shift()
alert(animals)